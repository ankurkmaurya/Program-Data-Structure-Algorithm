	public static List<Long> minOperations(int[] nums, int[] queries) {
	    List<Long> queriesMinOperations = new ArrayList<>(); 
	    
	    //Sort the Array first
	    Arrays.sort(nums);
	    
	    //Maintain the Sum of every index position of Sorted Array
	    long[] prefixSum = new long[nums.length];
	    for(int i=0; i<nums.length; i++) {
	    	if(i==0) {
	    		prefixSum[i] = nums[i];
	    		continue;
	    	}
	    	prefixSum[i] = prefixSum[i-1] + nums[i];
	    }
	    
	    //Iterate the queries array, Binary Search and find the minimum operation for each query
	    for(int query : queries) {
	    	int queryMinIndex = findFloorIndex(nums, query);
	    	int leftElementsCount = queryMinIndex+1;
	    	int rightElementsCount = nums.length-leftElementsCount ;
	    	
	    	long minOperationCount = 0;
	    	if(queryMinIndex>=0) {
	    		minOperationCount = (long)leftElementsCount*query - prefixSum[queryMinIndex];
	    		minOperationCount += (prefixSum[nums.length-1] - prefixSum[queryMinIndex]) - (long)rightElementsCount*query;
	    	} else {
	    		minOperationCount = prefixSum[nums.length-1] - (long)rightElementsCount*query;
	    	}
	    	queriesMinOperations.add(minOperationCount);
	    }
	    
	    return queriesMinOperations;
	}
	
	
	public static int findFloorIndex(int[] nums, int target) {
		int floorIndex = -1;
		
		int start = 0;
		int end = nums.length-1;
		while(start<=end) {
			int mid = start + (end -start)/2;
			
			if(nums[mid]<target) {
				start = mid+1;
				floorIndex = mid;
			}  
			else {
				end = mid-1;
			}
		}
		
		return floorIndex;
	}

