/*
 * 
 * Minimum Operations to Make All Array Elements Equal
 * 
 * Type : Medium
 * 
 * 
 * You are given an array nums consisting of positive integers.
 * You are also given an integer array queries of size m. 
 * For the ith query, you want to make all of the elements of nums equal to queries[i]. 
 * 
 * 
 * You can perform the following operation on the array any number of times:
 *   Increase or decrease an element of the array by 1.
 *   
 * Return an array answer of size m where answer[i] is the minimum number of operations to make 
 * all elements of nums equal to queries[i].
 * 
 * Note that after each query the array is reset to its original state.
 * 
 * 
 * 
 * Example 1:
 * Input: nums = [3,1,6,8], queries = [1,5,8,12]
 * Output: [14,10,14,30]
 * Explanation: For the first query we can do the following operations:
 *  - Decrease nums[0] 2 times, so that nums = [1,1,6,8]. 
 *  - Decrease nums[2] 5 times, so that nums = [1,1,1,8].
 *  - Decrease nums[3] 7 times, so that nums = [1,1,1,1].
 *  So the total number of operations for the first query is 2 + 5 + 7 = 14.
 *  For the second query we can do the following operations:
 *  - Increase nums[0] 2 times, so that nums = [5,1,6,8].
 *  - Increase nums[1] 4 times, so that nums = [5,5,6,8].
 *  - Decrease nums[2] 1 time, so that nums = [5,5,5,8].
 *  - Decrease nums[3] 3 times, so that nums = [5,5,5,5].
 *  So the total number of operations for the second query is 2 + 4 + 1 + 3 = 10.
 *  
 *  
 * Example 2:
 * Input: nums = [2,9,6,3], queries = [10]
 * Output: [20]
 * Explanation: We can increase each value in the array to 10. 
 * The total number of operations will be 8 + 1 + 4 + 7 = 20.
 * 
 * 
 * 
 * Constraints:
 * n == nums.length
 * m == queries.length
 * 1 <= n, m <= 10power5
 * 1 <= nums[i], queries[i] <= 10power9
 * 
 * 
 * 
 * 
 * Solution:
 * 
 * BruteForce:  
 * The problem can be solved with brute-force approach but the complexity will be 
 * O(num.length * query.length) == O(n*m)
 * 
 * 
 * Optimization: 
 * -- 57ms, Beats 48.08% [Sorting + Prefix Sum + Binary Search]
 * As we have to make the array elements equal to some target value we know that if the 
 * array elements are sorted then all the elements smaller than target will be increased and 
 * elements greater than target will be decreased so the array can be divided in to 3 parts where 
 * 1st part will be all the elements lesser than target, 2nd part will be the target itself if it 
 * exists and 3rd parts will be the elements greater than target.
 * So if we maintain the sum of all the elements till the ith position and if we know the position 
 * of the target element in the array then we can calculate the count of operations required to make 
 * the array elements equal to target by computing the operation count of all the 3 parts of the 
 * array by using the following formula:
 * 
 * - Part 1 : All elements lesser than target
 *   		  (Part 1 largest element position * target element) - 
 *   		  Sum of all the elements till the largest element in Part 1
 * 
 * - Part 2 : The target element itself if it exists
 *   		   0 as the element is same as the target number
 * 
 * - Part 3 : All elements greater than target
 *            Sum of all the elements till the end in Part 3 - 
 *            Sum of all the elements in Part 1 - 
 *            (Number of elements in Part3 * target element)
 * 
 * 
 * 
 * Time Complexity  : O(NlogN + N + MlogN) - for sorting nums + for prefix sum construction + for processing m queries using upper_bound
 *                    O(NlogN + MlogN)
 * 						
 * Space Complexity : O(N+M) - for prefix sum and result vectors
 * 
 * 
 * 
 * 
 * 
 * 
 */


